#include <stdio.h>

int main(void)
{

	int sum; // declaring the integer named "sum"

	sum = 50 + 25; // Declares that the value stored in sum is 50 + 25
	printf ("The sum of 50 and 25 is %i\n", sum); // %i calls an integer, sum is the integer to be called for the print out. It should print the results of 50+25.

	return 0;
}
